/// Simplify `base^(log(base, x))` subterms when splitting exponent sums.
pub fn simplify_exp_log(context: &mut Context, base: ExprId, exp: ExprId) -> ExprId {
    if let Expr::Function(name, args) = context.get(exp) {
        if context.is_builtin(*name, BuiltinFn::Log) && args.len() == 2 {
            let log_base = args[0];
            let log_arg = args[1];
            if log_base == base {
                return log_arg;
            }
        }
    }
    context.add(Expr::Pow(base, exp))
}

/// Rewrite `e^(a+b)` into product form when at least one side is logarithmic:
/// - `e^(a+b) -> e^a * e^b`
/// - matching `e^(log(e,x)+k)` style terms will simplify to `x*e^k`.
pub fn try_rewrite_split_log_exponents_expr(ctx: &mut Context, expr: ExprId) -> Option<LogRewrite> {
    let (base, exp) = as_pow(ctx, expr)?;
    if !is_e_constant_expr(ctx, base) {
        return None;
    }

    let (lhs, rhs) = as_add(ctx, exp)?;
    if !(is_log(ctx, lhs) || is_log(ctx, rhs)) {
        return None;
    }

    let term1 = simplify_exp_log(ctx, base, lhs);
    let term2 = simplify_exp_log(ctx, base, rhs);
    let rewritten = smart_mul(ctx, term1, term2);
    Some(LogRewrite {
        rewritten,
        desc: "e^(a+b) -> e^a * e^b (log cancellation)",
    })
}

/// Detect whether expression is a log/ln call (possibly nested in a product).
pub fn is_log(ctx: &Context, expr: ExprId) -> bool {
    if let Expr::Function(name, _) = ctx.get(expr) {
        if let Some(b) = ctx.builtin_of(*name) {
            return b == BuiltinFn::Log || b == BuiltinFn::Ln;
        }
    }
    if let Expr::Mul(l, r) = ctx.get(expr) {
        return is_log(ctx, *l) || is_log(ctx, *r);
    }
    false
}

/// Normalize expression to `(core, exponent)` form:
/// - `a` => `(a, 1)`
/// - `a^m` => `(a, m)`
/// - `1/a` => `(a, -1)`
pub fn normalize_to_power(ctx: &mut Context, expr: ExprId) -> (ExprId, ExprId) {
    match ctx.get(expr) {
        Expr::Pow(base, exp) => (*base, *exp),
        Expr::Div(num, den) => {
            let (num, den) = (*num, *den);
            if matches!(ctx.get(num), Expr::Number(n) if n.is_one()) {
                let neg_one = ctx.num(-1);
                (den, neg_one)
            } else {
                let one = ctx.num(1);
                (expr, one)
            }
        }
        _ => {
            let one = ctx.num(1);
            (expr, one)
        }
    }
}

/// Count multiplicative factors in a flattened multiplication tree.
pub fn count_mul_factors(ctx: &Context, expr: ExprId) -> u32 {
    match ctx.get(expr) {
        Expr::Mul(a, b) => count_mul_factors(ctx, *a) + count_mul_factors(ctx, *b),
        _ => 1,
    }
}

/// Collect multiplicative factors in left-to-right order from a multiplication tree.
pub fn collect_mul_factors(ctx: &Context, expr: ExprId) -> Vec<ExprId> {
    match ctx.get(expr) {
        Expr::Mul(a, b) => {
            let mut factors = collect_mul_factors(ctx, *a);
            factors.extend(collect_mul_factors(ctx, *b));
            factors
        }
        _ => vec![expr],
    }
}

/// Estimate how many terms would be generated by log expansion.
/// Returns `(base_terms, generated_terms, pow_exp_if_applicable)`.
pub fn estimate_log_terms(ctx: &Context, arg: ExprId) -> Option<(u32, u32, Option<u32>)> {
    match ctx.get(arg) {
        Expr::Mul(_, _) => {
            let factors = count_mul_factors(ctx, arg);
            if factors <= 1 {
                return None;
            }
            Some((factors, factors, None))
        }
        Expr::Div(num, den) => {
            let num_factors = count_mul_factors(ctx, *num);
            let den_factors = count_mul_factors(ctx, *den);
            let total = num_factors + den_factors;
            if total <= 1 {
                return None;
            }
            Some((total, total, None))
        }
        Expr::Pow(_, exp) => {
            if let Expr::Number(n) = ctx.get(*exp) {
                if n.is_integer() {
                    let exp_i64: i64 = n.to_integer().try_into().ok()?;
                    if exp_i64 > 0 {
                        let exp_u32 = exp_i64 as u32;
                        return Some((1, 1, Some(exp_u32)));
                    }
                }
            }
            None
        }
        _ => None,
    }
}

/// Evaluate `log_base(val)` as a rational number using prime factorization.
///
/// Returns `Some(ratio)` when both `base` and `val` are powers of the same
/// underlying prime support.
pub fn eval_log_rational(
    base: &num_bigint::BigInt,
    val: &num_bigint::BigInt,
) -> Option<num_rational::BigRational> {
    use num_bigint::BigInt;
    use num_rational::BigRational;
    use num_traits::{One, Signed, Zero};

    // Guard: base > 1, val > 0
    let one = BigInt::one();
    if base <= &one || val.is_zero() || val.is_negative() || base.is_negative() {
        return None;
    }

    // Special case: val = 1 => log_b(1) = 0
    if val.is_one() {
        return Some(BigRational::zero());
    }

    // Special case: base == val => log_b(b) = 1
    if base == val {
        return Some(BigRational::one());
    }

    let fb = prime_exponent_map(base);
    let fv = prime_exponent_map(val);

    if fb.keys().collect::<std::collections::HashSet<_>>()
        != fv.keys().collect::<std::collections::HashSet<_>>()
    {
        return None;
    }

    let mut ratio: Option<BigRational> = None;
    for (prime, exp_base) in &fb {
        let exp_val = fv.get(prime)?;
        if *exp_base == 0 {
            return None;
        }
        let r = BigRational::new(BigInt::from(*exp_val), BigInt::from(*exp_base));
        match &ratio {
            None => ratio = Some(r),
            Some(prev) if *prev == r => {}
            _ => return None,
        }
    }

    ratio
}

/// Rewrite/evaluate simple logarithm identities:
/// - `log(b, 1) -> 0`, `log(b, 0) -> -infinity`, `log(b, neg) -> undefined`
/// - numeric ratio evaluation when both base/arg are compatible integer powers
/// - `log(b, b) -> 1`
/// - `log(b, b^n) -> n` (numeric exponent)
/// - `log(b, x^y) -> y*log(b, x)` (non-inverse case; may require positivity assumption)
///
/// This function is purely structural/numeric and does not apply domain policy.
/// The caller is responsible for attaching assumption/require events.
pub fn try_rewrite_evaluate_log_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<EvaluateLogRewrite> {
    let (base, arg) = extract_log_base_argument(ctx, expr)?;

    if let Expr::Number(n) = ctx.get(arg) {
        let n = n.clone();
        if n.is_one() {
            let rewritten = ctx.num(0);
            return Some(EvaluateLogRewrite {
                rewritten,
                desc: "log(b, 1) = 0".to_string(),
                assume_positive_base: None,
            });
        }
        if n.is_zero() {
            let inf = ctx.add(Expr::Constant(Constant::Infinity));
            let rewritten = ctx.add(Expr::Neg(inf));
            return Some(EvaluateLogRewrite {
                rewritten,
                desc: "log(b, 0) = -infinity".to_string(),
                assume_positive_base: None,
            });
        }
        if n < num_rational::BigRational::zero() {
            let rewritten = ctx.add(Expr::Constant(Constant::Undefined));
            return Some(EvaluateLogRewrite {
                rewritten,
                desc: "log(b, neg) = undefined".to_string(),
                assume_positive_base: None,
            });
        }

        if let Expr::Number(b) = ctx.get(base) {
            let b = b.clone();
            if b.is_integer() && n.is_integer() {
                let b_int = b.to_integer();
                let n_int = n.to_integer();
                if let Some(ratio) = eval_log_rational(&b_int, &n_int) {
                    let rewritten = ctx.add(Expr::Number(ratio.clone()));
                    return Some(EvaluateLogRewrite {
                        rewritten,
                        desc: format!("log({}, {}) = {}", b, n, ratio),
                        assume_positive_base: None,
                    });
                }
            }
        }
    }

    if base == arg || ctx.get(base) == ctx.get(arg) {
        let rewritten = ctx.num(1);
        return Some(EvaluateLogRewrite {
            rewritten,
            desc: "log(b, b) = 1".to_string(),
            assume_positive_base: None,
        });
    }

    let Expr::Pow(p_base, p_exp) = ctx.get(arg) else {
        return None;
    };
    let (p_base, p_exp) = (*p_base, *p_exp);
    let is_inverse_composition =
        p_base == base || cas_ast::ordering::compare_expr(ctx, p_base, base) == Ordering::Equal;

    if is_inverse_composition {
        if matches!(ctx.get(p_exp), Expr::Number(_)) {
            return Some(EvaluateLogRewrite {
                rewritten: p_exp,
                desc: "log(b, b^n) = n".to_string(),
                assume_positive_base: None,
            });
        }
        return None;
    }

    let is_even_integer = match ctx.get(p_exp) {
        Expr::Number(n) if n.is_integer() => {
            let int_val = n.to_integer();
            let two: num_bigint::BigInt = 2.into();
            (&int_val % &two) == 0.into() && int_val != 0.into()
        }
        _ => false,
    };
    if is_even_integer {
        return None;
    }

    let log_inner = make_log_expr(ctx, base, p_base);
    let rewritten = smart_mul(ctx, p_exp, log_inner);
    Some(EvaluateLogRewrite {
        rewritten,
        desc: "log(b, x^y) = y * log(b, x)".to_string(),
        assume_positive_base: Some(p_base),
    })
}

/// Compute prime factorization map `prime -> exponent`.
pub fn prime_exponent_map(n: &num_bigint::BigInt) -> HashMap<num_bigint::BigInt, u32> {
    use num_bigint::BigInt;
    use num_integer::Integer;
    use num_traits::{One, Signed, Zero};

    let mut result = HashMap::new();
    let mut n = n.abs();
    let one = BigInt::one();

    if n <= one {
        return result;
    }

    let mut count_2 = 0u32;
    while n.is_even() {
        count_2 += 1;
        n /= 2;
    }
    if count_2 > 0 {
        result.insert(BigInt::from(2), count_2);
    }

    let mut d = BigInt::from(3);
    while &d * &d <= n {
        let mut count = 0u32;
        while (&n % &d).is_zero() {
            count += 1;
            n /= &d;
        }
        if count > 0 {
            result.insert(d.clone(), count);
        }
        d += 2;
    }

    if n > one {
        result.insert(n, 1);
    }

    result
}

