/// Plan positivity handling for auto-expanding logarithms.
///
/// The caller provides positivity predicates:
/// - `is_proven_positive`: algebraic proof oracle
/// - `is_implied_positive`: inherited-domain implication oracle
///
/// Behavior by mode:
/// - Strict: every factor must be proven positive
/// - Generic: every factor must be proven OR implied positive
/// - Assume: expansion is allowed, returning factors that need assumptions
pub fn plan_log_auto_expand_positivity(
    ctx: &Context,
    arg: ExprId,
    mode: LogAutoExpandMode,
    mut is_proven_positive: impl FnMut(ExprId) -> bool,
    mut is_implied_positive: impl FnMut(ExprId) -> bool,
) -> LogAutoExpandPositivityPlan {
    let factors = collect_mul_factors(ctx, arg);

    match mode {
        LogAutoExpandMode::Strict => {
            for factor in factors {
                if !is_proven_positive(factor) {
                    return LogAutoExpandPositivityPlan::Blocked { factor };
                }
            }
            LogAutoExpandPositivityPlan::AllowNoAssumptions
        }
        LogAutoExpandMode::Generic => {
            for factor in factors {
                if is_proven_positive(factor) || is_implied_positive(factor) {
                    continue;
                }
                return LogAutoExpandPositivityPlan::Blocked { factor };
            }
            LogAutoExpandPositivityPlan::AllowNoAssumptions
        }
        LogAutoExpandMode::Assume => {
            let mut assumptions = Vec::new();
            for factor in factors {
                if !is_proven_positive(factor) {
                    assumptions.push(factor);
                }
            }
            if assumptions.is_empty() {
                LogAutoExpandPositivityPlan::AllowNoAssumptions
            } else {
                LogAutoExpandPositivityPlan::AllowWithAssumptions(assumptions)
            }
        }
    }
}

/// Plan domain policy for symbolic `log(b, b^x) -> x` rewrites.
///
/// Inputs are fully-decided booleans from the engine side so this function stays
/// domain-agnostic and reusable.
pub fn plan_log_exp_inverse_symbolic_policy(
    mode: LogExpInversePolicyMode,
    complex_enabled: bool,
    is_e_base: bool,
    base_positive_proven: bool,
    base_is_one: bool,
) -> LogExpInversePolicyPlan {
    if complex_enabled {
        return LogExpInversePolicyPlan::Block;
    }

    if is_e_base {
        return LogExpInversePolicyPlan::Rewrite {
            assume_positive_base: false,
        };
    }

    if !base_positive_proven {
        return match mode {
            LogExpInversePolicyMode::Strict | LogExpInversePolicyMode::Generic => {
                LogExpInversePolicyPlan::Block
            }
            LogExpInversePolicyMode::Assume => LogExpInversePolicyPlan::Rewrite {
                assume_positive_base: true,
            },
        };
    }

    if base_is_one {
        return LogExpInversePolicyPlan::Block;
    }

    LogExpInversePolicyPlan::Rewrite {
        assume_positive_base: false,
    }
}

/// Plan domain policy for `b^log(b, x)` family rewrites.
pub fn plan_exponential_log_inverse_policy(
    mode: LogExpInversePolicyMode,
    positive_subject_proven: bool,
) -> ExponentialLogInversePolicyPlan {
    match mode {
        LogExpInversePolicyMode::Strict if !positive_subject_proven => {
            ExponentialLogInversePolicyPlan::Block
        }
        _ => ExponentialLogInversePolicyPlan::Rewrite {
            require_positive_subject: !positive_subject_proven,
        },
    }
}

/// Plan domain policy for numeric `log(a^m, a^n) -> n/m` rewrites.
pub fn plan_log_power_base_numeric_policy(
    mode: LogExpInversePolicyMode,
    complex_enabled: bool,
    base_positive_proven: bool,
    base_is_one: bool,
) -> LogPowerBasePolicyPlan {
    if complex_enabled {
        return LogPowerBasePolicyPlan::Block;
    }

    match mode {
        LogExpInversePolicyMode::Strict => {
            if !base_positive_proven || base_is_one {
                LogPowerBasePolicyPlan::Block
            } else {
                LogPowerBasePolicyPlan::Rewrite {
                    require_positive_base: false,
                    require_nonzero_base_minus_one: true,
                }
            }
        }
        LogExpInversePolicyMode::Generic => LogPowerBasePolicyPlan::Rewrite {
            require_positive_base: false,
            require_nonzero_base_minus_one: true,
        },
        LogExpInversePolicyMode::Assume => LogPowerBasePolicyPlan::Rewrite {
            require_positive_base: !base_positive_proven,
            require_nonzero_base_minus_one: true,
        },
    }
}

/// Build canonical log expression:
/// - `base == sentinel_log10` => `log(arg)`
/// - `base == e` => `ln(arg)`
/// - otherwise => `log(base, arg)`
pub fn make_log_expr(ctx: &mut Context, base: ExprId, arg: ExprId) -> ExprId {
    // Sentinel reserved by engine layer for base-10 log.
    let sentinel_log10 = ExprId::from_raw(u32::MAX - 1);
    if base == sentinel_log10 {
        return ctx.call_builtin(BuiltinFn::Log, vec![arg]);
    }
    if is_e_constant_expr(ctx, base) {
        ctx.call_builtin(BuiltinFn::Ln, vec![arg])
    } else {
        ctx.call_builtin(BuiltinFn::Log, vec![base, arg])
    }
}

/// Sentinel used by legacy engine log rules to represent the `ln` base.
#[inline]
pub fn ln_base_sentinel() -> ExprId {
    ExprId::from_raw(u32::MAX)
}

/// Extract `(base, argument)` from a log expression.
///
/// Returns:
/// - `(ln_base_sentinel(), arg)` for `ln(arg)`
/// - `(base, arg)` for `log(base, arg)`
pub fn try_extract_log_parts(ctx: &Context, expr: ExprId) -> Option<(ExprId, ExprId)> {
    let Expr::Function(fn_id, args) = ctx.get(expr) else {
        return None;
    };

    match ctx.builtin_of(*fn_id) {
        Some(BuiltinFn::Ln) if args.len() == 1 => Some((ln_base_sentinel(), args[0])),
        Some(BuiltinFn::Log) if args.len() == 2 => Some((args[0], args[1])),
        _ => None,
    }
}

/// Compare two expressions for logarithm-base matching semantics.
///
/// Special handling:
/// - `ExprId::from_raw(u32::MAX)` is treated as ln-base sentinel and matches `e`.
pub fn exprs_match_for_logs(ctx: &Context, e1: ExprId, e2: ExprId) -> bool {
    let sentinel = ln_base_sentinel();

    if e1 == sentinel {
        return is_e_constant_expr(ctx, e2);
    }
    if e2 == sentinel {
        return is_e_constant_expr(ctx, e1);
    }

    if e1 == e2 {
        return true;
    }

    cas_ast::ordering::compare_expr(ctx, e1, e2) == Ordering::Equal
}

/// Compare log bases with ln-sentinel compatibility.
pub fn bases_equal_for_logs(ctx: &Context, base_l: ExprId, base_r: ExprId) -> bool {
    let sentinel = ln_base_sentinel();

    if base_l == sentinel && base_r == sentinel {
        return true;
    }
    if base_l == sentinel {
        return is_e_constant_expr(ctx, base_r);
    }
    if base_r == sentinel {
        return is_e_constant_expr(ctx, base_l);
    }

    if base_l == base_r {
        return true;
    }

    cas_ast::ordering::compare_expr(ctx, base_l, base_r) == Ordering::Equal
}

/// Rewrite `ln(e*x)` or `ln(x*e)` to `1 + ln(x)`.
pub fn try_rewrite_ln_e_product_expr(ctx: &mut Context, expr: ExprId) -> Option<LnEProductRewrite> {
    let Expr::Function(fn_id, args) = ctx.get(expr) else {
        return None;
    };
    let fn_id = *fn_id;
    let args = args.clone();
    if ctx.builtin_of(fn_id) != Some(BuiltinFn::Ln) || args.len() != 1 {
        return None;
    }
    let arg = args[0];

    let Expr::Mul(l, r) = ctx.get(arg) else {
        return None;
    };
    let (l, r) = (*l, *r);
    let l_is_e = is_e_constant_expr(ctx, l);
    let r_is_e = is_e_constant_expr(ctx, r);

    let x = if l_is_e {
        Some(r)
    } else if r_is_e {
        Some(l)
    } else {
        None
    }?;

    let one = ctx.num(1);
    let ln_x = ctx.call_builtin(BuiltinFn::Ln, vec![x]);
    let rewritten = ctx.add(Expr::Add(one, ln_x));
    Some(LnEProductRewrite { rewritten })
}

/// Rewrite:
/// - `ln(x/e)` -> `ln(x) - 1`
/// - `ln(e/x)` -> `1 - ln(x)`
pub fn try_rewrite_ln_e_div_expr(ctx: &mut Context, expr: ExprId) -> Option<LnEDivRewrite> {
    let Expr::Function(fn_id, args) = ctx.get(expr) else {
        return None;
    };
    let fn_id = *fn_id;
    let args = args.clone();
    if ctx.builtin_of(fn_id) != Some(BuiltinFn::Ln) || args.len() != 1 {
        return None;
    }
    let arg = args[0];

    let Expr::Div(num, den) = ctx.get(arg) else {
        return None;
    };
    let (num, den) = (*num, *den);
    let num_is_e = is_e_constant_expr(ctx, num);
    let den_is_e = is_e_constant_expr(ctx, den);

    if den_is_e && !num_is_e {
        let ln_x = ctx.call_builtin(BuiltinFn::Ln, vec![num]);
        let one = ctx.num(1);
        let rewritten = ctx.add(Expr::Sub(ln_x, one));
        return Some(LnEDivRewrite {
            rewritten,
            kind: LnEDivRewriteKind::XOverE,
        });
    }

    if num_is_e && !den_is_e {
        let one = ctx.num(1);
        let ln_x = ctx.call_builtin(BuiltinFn::Ln, vec![den]);
        let rewritten = ctx.add(Expr::Sub(one, ln_x));
        return Some(LnEDivRewrite {
            rewritten,
            kind: LnEDivRewriteKind::EOverX,
        });
    }

    None
}

/// Contract pairwise logarithm additions/subtractions:
/// - `ln(a) + ln(b) -> ln(a*b)`
/// - `ln(a) - ln(b) -> ln(a/b)`
/// - `log(b, x) + log(b, y) -> log(b, x*y)` for equal bases
/// - `log(b, x) - log(b, y) -> log(b, x/y)` for equal bases
pub fn try_rewrite_log_contraction_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogContractionRewrite> {
    match ctx.get(expr) {
        Expr::Add(lhs, rhs) => {
            let (lhs, rhs) = (*lhs, *rhs);
            let (base_l, arg_l) = try_extract_log_parts(ctx, lhs)?;
            let (base_r, arg_r) = try_extract_log_parts(ctx, rhs)?;
            if !bases_equal_for_logs(ctx, base_l, base_r) {
                return None;
            }

            let product = ctx.add(Expr::Mul(arg_l, arg_r));
            let rewritten = if base_l == ln_base_sentinel() {
                ctx.call_builtin(BuiltinFn::Ln, vec![product])
            } else {
                make_log_expr(ctx, base_l, product)
            };
            Some(LogContractionRewrite {
                rewritten,
                kind: LogContractionRewriteKind::Add,
            })
        }
        Expr::Sub(lhs, rhs) => {
            let (lhs, rhs) = (*lhs, *rhs);
            let (base_l, arg_l) = try_extract_log_parts(ctx, lhs)?;
            let (base_r, arg_r) = try_extract_log_parts(ctx, rhs)?;
            if !bases_equal_for_logs(ctx, base_l, base_r) {
                return None;
            }

            let quotient = ctx.add(Expr::Div(arg_l, arg_r));
            let rewritten = if base_l == ln_base_sentinel() {
                ctx.call_builtin(BuiltinFn::Ln, vec![quotient])
            } else {
                make_log_expr(ctx, base_l, quotient)
            };
            Some(LogContractionRewrite {
                rewritten,
                kind: LogContractionRewriteKind::Sub,
            })
        }
        _ => None,
    }
}

/// Expand one-step logarithm product/quotient:
/// - `log(b, x*y) -> log(b, x) + log(b, y)`
/// - `log(b, x/y) -> log(b, x) - log(b, y)`
///
/// Returns both factors that require positivity checks (`x`, `y`) so caller
/// can apply domain-policy/assumption handling outside this helper.
pub fn try_rewrite_log_mul_div_expansion_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogMulDivExpansionRewrite> {
    let (base, arg) = extract_log_base_argument(ctx, expr)?;

    if let Some((lhs, rhs)) = as_mul(ctx, arg) {
        let log_lhs = make_log_expr(ctx, base, lhs);
        let log_rhs = make_log_expr(ctx, base, rhs);
        let rewritten = ctx.add(Expr::Add(log_lhs, log_rhs));
        return Some(LogMulDivExpansionRewrite {
            rewritten,
            positive_lhs: lhs,
            positive_rhs: rhs,
            desc: "log(b, x*y) = log(b, x) + log(b, y)",
        });
    }

    if let Some((num, den)) = as_div(ctx, arg) {
        let log_num = make_log_expr(ctx, base, num);
        let log_den = make_log_expr(ctx, base, den);
        let rewritten = ctx.add(Expr::Sub(log_num, log_den));
        return Some(LogMulDivExpansionRewrite {
            rewritten,
            positive_lhs: num,
            positive_rhs: den,
            desc: "log(b, x/y) = log(b, x) - log(b, y)",
        });
    }

    None
}

/// Rewrite perfect-square logarithm arguments:
/// - `ln((A ± B)^2) -> 2*ln(|A ± B|)`
/// - `log(base, (A ± B)^2) -> 2*log(base, |A ± B|)`
///
/// Includes detectors for:
/// - coefficient-bearing quadratic trinomials with discriminant 0
/// - AST-level perfect-square trinomial matching
/// - even powers, monomial squares, and quotient of squares
pub fn try_rewrite_log_perfect_square_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogPerfectSquareRewrite> {
    let fn_id = match ctx.get(expr) {
        Expr::Function(fn_id, _) => *fn_id,
        _ => return None,
    };

    let (log_base, arg) = match ctx.builtin_of(fn_id) {
        Some(BuiltinFn::Ln) | Some(BuiltinFn::Log) => extract_log_base_argument(ctx, expr)?,
        _ => return None,
    };

    let build_result = |ctx: &mut Context,
                        factor: ExprId,
                        log_base: ExprId,
                        desc: &'static str|
     -> Option<LogPerfectSquareRewrite> {
        let abs_factor = ctx.call_builtin(BuiltinFn::Abs, vec![factor]);
        let log_inner = make_log_expr(ctx, log_base, abs_factor);
        let two = ctx.num(2);
        let rewritten = ctx.add(Expr::Mul(two, log_inner));
        Some(LogPerfectSquareRewrite { rewritten, desc })
    };

    if matches!(ctx.get(arg), Expr::Add(_, _) | Expr::Sub(_, _)) {
        let vars = cas_ast::collect_variables(ctx, arg);
        if vars.len() == 1 {
            let var = vars.iter().next()?;
            if let Ok(poly) = crate::polynomial::Polynomial::from_expr(ctx, arg, var) {
                if poly.degree() == 2 && poly.coeffs.len() >= 3 {
                    let pa = poly.coeffs.get(2).cloned();
                    let pb = poly.coeffs.get(1).cloned();
                    let pc = poly.coeffs.first().cloned();

                    if let (Some(pa), Some(pb), Some(pc)) = (pa, pb, pc) {
                        let four = num_rational::BigRational::from_integer(4.into());
                        let discriminant = pb.clone() * pb.clone() - four * pa.clone() * pc.clone();

                        if discriminant.is_zero() {
                            if let Some(d) = rational_sqrt(&pa) {
                                let two_r = num_rational::BigRational::from_integer(2.into());
                                let e = if d.is_zero() {
                                    rational_sqrt(&pc)
                                        .unwrap_or_else(num_rational::BigRational::zero)
                                } else {
                                    pb / (two_r * d.clone())
                                };

                                let var_expr = ctx.var(var);
                                let d_expr = ctx.add(Expr::Number(d.clone()));
                                let e_expr = ctx.add(Expr::Number(e.clone()));
                                let one = num_rational::BigRational::from_integer(1.into());

                                let dx = if d == one {
                                    var_expr
                                } else {
                                    smart_mul(ctx, d_expr, var_expr)
                                };

                                let linear = if e.is_zero() {
                                    dx
                                } else {
                                    ctx.add(Expr::Add(dx, e_expr))
                                };

                                return build_result(
                                    ctx,
                                    linear,
                                    log_base,
                                    "ln((A+B)^2) = 2·ln(|A+B|)",
                                );
                            }
                        }
                    }
                }
            }
        }

        if let Some((a, b, is_sub)) = try_match_perfect_square_trinomial(ctx, arg) {
            let factor = if is_sub {
                ctx.add(Expr::Sub(a, b))
            } else {
                ctx.add(Expr::Add(a, b))
            };
            let desc = if is_sub {
                "ln((A-B)^2) = 2·ln(|A-B|)"
            } else {
                "ln((A+B)^2) = 2·ln(|A+B|)"
            };
            return build_result(ctx, factor, log_base, desc);
        }
    }

    if let Expr::Pow(p_base, p_exp) = ctx.get(arg) {
        let (p_base, p_exp) = (*p_base, *p_exp);
        if cas_ast::ordering::compare_expr(ctx, p_base, log_base) == Ordering::Equal {
            return None;
        }

        if let Expr::Number(n) = ctx.get(p_exp) {
            let n = n.clone();
            if n.is_integer() {
                let int_val = n.to_integer();
                let two_bi: num_bigint::BigInt = 2.into();
                if &int_val % &two_bi == 0.into() && int_val > 0.into() {
                    let half = &int_val / &two_bi;
                    let half_rat = num_rational::BigRational::from_integer(half.clone());
                    let factor = if half == 1.into() {
                        p_base
                    } else {
                        let half_id = ctx.add(Expr::Number(half_rat));
                        ctx.add(Expr::Pow(p_base, half_id))
                    };
                    return build_result(ctx, factor, log_base, "ln(x^(2k)) = 2·ln(|x^k|)");
                }
            }
        }
    }

    if let Expr::Mul(_, _) = ctx.get(arg) {
        if let Some(factor) = extract_square_root_of_term(ctx, arg) {
            return build_result(ctx, factor, log_base, "ln((c*x)^2) = 2·ln(|c*x|)");
        }
    }

    if let Expr::Div(num, den) = ctx.get(arg) {
        let (num, den) = (*num, *den);
        if let (Some(num_root), Some(den_root)) = (
            extract_square_root_of_term(ctx, num),
            extract_square_root_of_term(ctx, den),
        ) {
            let factor = ctx.add(Expr::Div(num_root, den_root));
            return build_result(ctx, factor, log_base, "ln(a^2/b^2) = 2·ln(|a/b|)");
        }
    }

    None
}

/// Plan `log(base, x^(even))` rewrite candidates:
/// - with absolute value: `even * log(base, |x|)`
/// - without absolute value: `even * log(base, x)`
///
/// Structural gates:
/// - argument must be a power with even integer exponent
/// - skip inverse compositions (`log(b, b^x)`)
/// - skip `ln(exp(...)^n)` to let inverse-exp rules fire first
pub fn try_plan_log_even_power_abs_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogEvenPowerAbsPlan> {
    let (base, arg) = extract_log_base_argument(ctx, expr)?;
    let (inner_base, exponent) = as_pow(ctx, arg)?;

    if cas_ast::ordering::compare_expr(ctx, inner_base, base) == Ordering::Equal {
        return None;
    }

    if is_e_constant_expr(ctx, base)
        && matches!(ctx.get(inner_base), Expr::Function(fname, _) if ctx.is_builtin(*fname, BuiltinFn::Exp))
    {
        return None;
    }

    let is_even_integer = match ctx.get(exponent) {
        Expr::Number(n) if n.is_integer() => {
            let int_val = n.to_integer();
            let two: num_bigint::BigInt = 2.into();
            (&int_val % &two) == 0.into() && int_val != 0.into()
        }
        _ => false,
    };
    if !is_even_integer {
        return None;
    }

    let abs_inner_base = ctx.call_builtin(BuiltinFn::Abs, vec![inner_base]);
    let with_abs_log = make_log_expr(ctx, base, abs_inner_base);
    let without_abs_log = make_log_expr(ctx, base, inner_base);

    Some(LogEvenPowerAbsPlan {
        inner_base,
        exponent,
        abs_inner_base,
        with_abs_rewrite: smart_mul(ctx, exponent, with_abs_log),
        without_abs_rewrite: smart_mul(ctx, exponent, without_abs_log),
    })
}

/// Rewrite `log(base, |u|^n)` with positive integer `n`:
/// - `log(base, |u|^n) -> n * log(base, |u|)`
///
/// Returns the inner subject `u` so caller can attach non-zero hints/requirements.
pub fn try_rewrite_log_abs_power_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogAbsPowerRewrite> {
    let (base, arg) = extract_log_base_argument(ctx, expr)?;
    let (pow_base, pow_exp) = as_pow(ctx, arg)?;

    let Expr::Function(abs_name, abs_args) = ctx.get(pow_base) else {
        return None;
    };
    if !ctx.is_builtin(*abs_name, BuiltinFn::Abs) || abs_args.len() != 1 {
        return None;
    }
    let inner_subject = abs_args[0];

    let is_positive_integer = match ctx.get(pow_exp) {
        Expr::Number(n) if n.is_integer() => {
            let int_val = n.to_integer();
            int_val > 0.into()
        }
        _ => false,
    };
    if !is_positive_integer {
        return None;
    }

    let log_abs = make_log_expr(ctx, base, pow_base);
    Some(LogAbsPowerRewrite {
        rewritten: smart_mul(ctx, pow_exp, log_abs),
        inner_subject,
        desc: "ln(|u|^n) = n·ln(|u|)",
    })
}

/// Rewrite `log(base, |x|)` / `ln(|x|)` into `log(base, x)` / `ln(x)`.
///
/// This is structural-only and does not enforce domain policy.
/// Callers should apply `plan_log_abs_simplify_policy` before committing rewrite.
pub fn try_rewrite_log_abs_simplify_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogAbsSimplifyRewrite> {
    let (base_opt, arg) = extract_log_base_argument_view(ctx, expr)?;

    let inner_subject = match ctx.get(arg) {
        Expr::Function(fn_id, args)
            if ctx.is_builtin(*fn_id, BuiltinFn::Abs) && args.len() == 1 =>
        {
            args[0]
        }
        _ => return None,
    };

    let rewritten = match base_opt {
        Some(base) => ctx.call_builtin(BuiltinFn::Log, vec![base, inner_subject]),
        None => ctx.call_builtin(BuiltinFn::Ln, vec![inner_subject]),
    };

    Some(LogAbsSimplifyRewrite {
        rewritten,
        inner_subject,
    })
}

/// Plan domain policy for simplifying `log(base, |x|)` / `ln(|x|)` to `log(base, x)` / `ln(x)`.
///
/// `complex_enabled`: true when value-domain is complex.
/// `positive_subject_proven`: true when positivity of `x` is proven.
pub fn plan_log_abs_simplify_policy(
    mode: LogExpInversePolicyMode,
    complex_enabled: bool,
    positive_subject_proven: bool,
) -> LogAbsSimplifyPolicyPlan {
    if complex_enabled {
        return if positive_subject_proven {
            LogAbsSimplifyPolicyPlan::Rewrite {
                assume_positive_subject: false,
                desc: "ln(|x|) = ln(x) for x > 0",
            }
        } else {
            LogAbsSimplifyPolicyPlan::Block
        };
    }

    match mode {
        LogExpInversePolicyMode::Assume => LogAbsSimplifyPolicyPlan::Rewrite {
            assume_positive_subject: true,
            desc: "ln(|x|) = ln(x) (assuming x > 0)",
        },
        LogExpInversePolicyMode::Strict | LogExpInversePolicyMode::Generic => {
            if positive_subject_proven {
                LogAbsSimplifyPolicyPlan::Rewrite {
                    assume_positive_subject: false,
                    desc: "ln(|x|) = ln(x) for x > 0",
                }
            } else {
                LogAbsSimplifyPolicyPlan::Block
            }
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum LogEvenPowerPolicyPlan {
    Block,
    RewriteWithChain {
        chain_desc: &'static str,
        assume_positive_subject: bool,
    },
    RewriteWithAbsAssume {
        assume_positive_subject: bool,
    },
}

/// Plan policy for rewriting `log(base, x^(even))` through an absolute-value step:
/// - with chain: `even*log(base, |x|)` then `|x| -> x`
/// - without chain in Assume mode: keep `even*log(base, |x|)` with assumption
pub fn plan_log_even_power_policy(
    mode: LogExpInversePolicyMode,
    positive_subject_proven: bool,
    positive_subject_disproven: bool,
    in_requires: bool,
) -> LogEvenPowerPolicyPlan {
    let can_chain = match mode {
        LogExpInversePolicyMode::Strict | LogExpInversePolicyMode::Generic => {
            positive_subject_proven || in_requires
        }
        LogExpInversePolicyMode::Assume => !positive_subject_disproven,
    };

    if can_chain {
        let assume_positive_subject =
            !positive_subject_proven && !in_requires && mode == LogExpInversePolicyMode::Assume;
        let chain_desc = if positive_subject_proven || in_requires {
            "|x| = x for x > 0"
        } else {
            "|x| = x (assuming x > 0)"
        };
        return LogEvenPowerPolicyPlan::RewriteWithChain {
            chain_desc,
            assume_positive_subject,
        };
    }

    match mode {
        LogExpInversePolicyMode::Strict | LogExpInversePolicyMode::Generic => {
            LogEvenPowerPolicyPlan::Block
        }
        LogExpInversePolicyMode::Assume => LogEvenPowerPolicyPlan::RewriteWithAbsAssume {
            assume_positive_subject: true,
        },
    }
}

/// Whether looking up inherited positivity (`in_requires`) can change the
/// `log(x^(even))` policy outcome.
///
/// This allows callers to skip expensive implicit-domain inference when it
/// cannot affect the decision.
pub fn log_even_power_needs_requires_lookup(
    mode: LogExpInversePolicyMode,
    positive_subject_proven: bool,
    positive_subject_disproven: bool,
) -> bool {
    match mode {
        LogExpInversePolicyMode::Strict | LogExpInversePolicyMode::Generic => {
            !positive_subject_proven
        }
        LogExpInversePolicyMode::Assume => !positive_subject_proven && !positive_subject_disproven,
    }
}

/// Contract chain products of logarithms:
/// - `log(b, a) * log(a, c) -> log(b, c)`
pub fn try_rewrite_log_chain_product_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogChainProductRewrite> {
    if !matches!(ctx.get(expr), Expr::Mul(_, _)) {
        return None;
    }

    let factors: Vec<ExprId> = MulView::from_expr(ctx, expr).factors.into_iter().collect();
    let log_parts: Vec<Option<(ExprId, ExprId)>> = factors
        .iter()
        .map(|&factor| try_extract_log_parts(ctx, factor))
        .collect();

    for (i, log_i_opt) in log_parts.iter().enumerate() {
        let Some((base_i, arg_i)) = log_i_opt else {
            continue;
        };

        for (j, log_j_opt) in log_parts.iter().enumerate() {
            if i == j {
                continue;
            }
            let Some((base_j, arg_j)) = log_j_opt else {
                continue;
            };

            if !exprs_match_for_logs(ctx, *arg_i, *base_j) {
                continue;
            }

            let new_log = make_log_expr(ctx, *base_i, *arg_j);
            let mut product = new_log;
            let mut had_remaining = false;
            for (idx, &factor) in factors.iter().enumerate() {
                if idx == i || idx == j {
                    continue;
                }
                had_remaining = true;
                product = smart_mul(ctx, product, factor);
            }

            let rewritten = if had_remaining { product } else { new_log };
            return Some(LogChainProductRewrite {
                rewritten,
                kind: LogChainProductRewriteKind::Telescoping,
            });
        }
    }

    None
}

/// Rewrite exponential-log inverse compositions:
/// - `b^log(b, x) -> x`
/// - `b^(-log(b, x)) -> x^(-1)`
/// - `b^(c*log(b, x)) -> x^c`
///
/// Returns the rewritten expression plus the subject that must be positive for
/// domain-safe application (`x` above).
pub fn try_rewrite_exponential_log_inverse_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<ExponentialLogInverseRewrite> {
    let (base, exp) = as_pow(ctx, expr)?;

    let matches_base = |ctx: &Context, log_base: ExprId, target_base: ExprId| {
        cas_ast::ordering::compare_expr(ctx, log_base, target_base) == Ordering::Equal
    };

    if let Some((log_base, log_arg)) = extract_log_base_argument(ctx, exp) {
        if matches_base(ctx, log_base, base) {
            return Some(ExponentialLogInverseRewrite {
                rewritten: log_arg,
                positive_subject: log_arg,
                desc: "b^log(b, x) = x",
            });
        }
    }

    if let Expr::Neg(neg_inner) = ctx.get(exp) {
        let neg_inner = *neg_inner;
        if let Some((log_base, log_arg)) = extract_log_base_argument(ctx, neg_inner) {
            if matches_base(ctx, log_base, base) {
                let neg_one = ctx.num(-1);
                let rewritten = ctx.add(Expr::Pow(log_arg, neg_one));
                return Some(ExponentialLogInverseRewrite {
                    rewritten,
                    positive_subject: log_arg,
                    desc: "b^(-log(b, x)) = 1/x",
                });
            }
        }
    }

    let (lhs, rhs) = as_mul(ctx, exp)?;
    let try_side = |ctx: &mut Context,
                    target: ExprId,
                    coeff: ExprId,
                    target_base: ExprId|
     -> Option<ExponentialLogInverseRewrite> {
        let (log_base, log_arg) = extract_log_base_argument(ctx, target)?;
        if cas_ast::ordering::compare_expr(ctx, log_base, target_base) != Ordering::Equal {
            return None;
        }
        let rewritten = ctx.add(Expr::Pow(log_arg, coeff));
        Some(ExponentialLogInverseRewrite {
            rewritten,
            positive_subject: log_arg,
            desc: "b^(c*log(b, x)) = x^c",
        })
    };

    try_side(ctx, lhs, rhs, base).or_else(|| try_side(ctx, rhs, lhs, base))
}

/// Match `log(b, b^x)` / `ln(e^x)` inverse compositions.
///
/// Returns:
/// - `Numeric` when exponent is numeric (`log(b, b^n) -> n` always safe)
/// - `Symbolic` when exponent is symbolic and engine policy must decide.
pub fn try_match_log_exp_inverse_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogExpInverseMatch> {
    let (base, arg) = extract_log_base_argument(ctx, expr)?;
    let (pow_base, pow_exp) = as_pow(ctx, arg)?;

    if cas_ast::ordering::compare_expr(ctx, pow_base, base) != Ordering::Equal {
        return None;
    }

    if matches!(ctx.get(pow_exp), Expr::Number(_)) {
        Some(LogExpInverseMatch::Numeric {
            rewritten: pow_exp,
            desc: "log(b, b^n) = n",
        })
    } else {
        Some(LogExpInverseMatch::Symbolic {
            base,
            exponent: pow_exp,
        })
    }
}

/// Rewrite numeric `log(a^m, a^n) -> n/m`.
///
/// This only performs structural/numeric normalization and ratio computation.
/// Domain gating (`a>0`, `a!=1`, real/complex mode) remains engine policy.
pub fn try_rewrite_log_power_base_numeric_expr(
    ctx: &mut Context,
    expr: ExprId,
) -> Option<LogPowerBaseNumericRewrite> {
    let Expr::Function(fn_id, args) = ctx.get(expr) else {
        return None;
    };
    let fn_id = *fn_id;
    let args = args.clone();

    if ctx.builtin_of(fn_id) != Some(BuiltinFn::Log) || args.len() != 2 {
        return None;
    }
    let base = args[0];
    let arg = args[1];

    let (base_core, base_exp) = normalize_to_power(ctx, base);
    let (arg_core, arg_exp) = normalize_to_power(ctx, arg);

    if cas_ast::ordering::compare_expr(ctx, base_core, arg_core) != Ordering::Equal {
        return None;
    }

    let base_exp_is_one = matches!(ctx.get(base_exp), Expr::Number(n) if n.is_one());
    if base_exp_is_one {
        return None;
    }

    let Expr::Number(m) = ctx.get(base_exp) else {
        return None;
    };
    let Expr::Number(n) = ctx.get(arg_exp) else {
        return None;
    };

    let m = m.clone();
    let n = n.clone();
    if m.is_zero() {
        return None;
    }

    let result_ratio = n.clone() / m.clone();
    let rewritten = ctx.add(Expr::Number(result_ratio.clone()));
    let m_disp = m.clone();
    let n_disp = n.clone();
    let desc = format!(
        "log(a^{}, a^{}) = {}/{} = {}",
        m_disp, n_disp, n_disp, m_disp, result_ratio
    );

    Some(LogPowerBaseNumericRewrite {
        rewritten,
        base_core,
        base_expr: base,
        desc,
    })
}

/// Rewrite `x^(c/log(b, x))` patterns:
/// - `x^(c / log(b, x)) -> b^c`
/// - `x^(c / ln(x)) -> e^c`
/// - `x^(c * log(b, x)^-1) -> b^c`
pub fn try_rewrite_log_inverse_power_expr(ctx: &mut Context, expr: ExprId) -> Option<LogRewrite> {
    fn is_minus_one_power(ctx: &Context, expr: ExprId) -> bool {
        matches!(
            ctx.get(expr),
            Expr::Number(n)
                if n.is_integer()
                    && *n == num_rational::BigRational::from_integer((-1).into())
        )
    }

    let (base, exp) = as_pow(ctx, expr)?;

    let check_log_denom = |ctx: &Context, denom: ExprId| -> Option<Option<ExprId>> {
        let (log_base_opt, log_arg) = extract_log_base_argument_view(ctx, denom)?;
        if cas_ast::ordering::compare_expr(ctx, log_arg, base) == Ordering::Equal {
            Some(log_base_opt)
        } else {
            None
        }
    };

    let mut target_base_opt: Option<Option<ExprId>> = None;
    let mut coeff: Option<ExprId> = None;

    if let Some((num, den)) = as_div(ctx, exp) {
        if let Some(base_opt) = check_log_denom(ctx, den) {
            target_base_opt = Some(base_opt);
            coeff = Some(num);
        }
    } else if let Some((lhs, rhs)) = as_mul(ctx, exp) {
        if let Some((den, den_exp)) = as_pow(ctx, rhs) {
            if is_minus_one_power(ctx, den_exp) {
                if let Some(base_opt) = check_log_denom(ctx, den) {
                    target_base_opt = Some(base_opt);
                    coeff = Some(lhs);
                }
            }
        }

        if target_base_opt.is_none() {
            if let Some((den, den_exp)) = as_pow(ctx, lhs) {
                if is_minus_one_power(ctx, den_exp) {
                    if let Some(base_opt) = check_log_denom(ctx, den) {
                        target_base_opt = Some(base_opt);
                        coeff = Some(rhs);
                    }
                }
            }
        }
    } else if let Some((den, den_exp)) = as_pow(ctx, exp) {
        if is_minus_one_power(ctx, den_exp) {
            if let Some(base_opt) = check_log_denom(ctx, den) {
                target_base_opt = Some(base_opt);
                coeff = Some(ctx.num(1));
            }
        }
    }

    let (base_opt, c) = (target_base_opt?, coeff?);
    let target_base = base_opt.unwrap_or_else(|| ctx.add(Expr::Constant(Constant::E)));
    let rewritten = ctx.add(Expr::Pow(target_base, c));
    Some(LogRewrite {
        rewritten,
        desc: "x^(c/log(b, x)) = b^c",
    })
}

/// Expand logarithm arguments for auto-expand workflows:
/// - `log(b, a*b*c) -> log(b,a) + log(b,b) + log(b,c)`
/// - `log(b, a/b) -> log(b,a) - log(b,b)`
/// - `log(b, u^n) -> n*log(b,u)`
pub fn try_expand_log_auto_rule_expr(
    ctx: &mut Context,
    original: ExprId,
    arg: ExprId,
) -> Option<LogRewrite> {
    let (base_opt, _) = extract_log_base_argument_relaxed_view(ctx, original)?;
    let base = match base_opt {
        Some(base) => base,
        None => match ctx.get(original) {
            Expr::Function(fn_id, _) if ctx.is_builtin(*fn_id, BuiltinFn::Ln) => {
                ctx.add(Expr::Constant(Constant::E))
            }
            Expr::Function(fn_id, _) if ctx.is_builtin(*fn_id, BuiltinFn::Log) => {
                // `log(x)` base-10 sentinel used by engine/parser bridge.
                ExprId::from_raw(u32::MAX - 1)
            }
            _ => return None,
        },
    };

    match ctx.get(arg) {
        Expr::Mul(_, _) => {
            let factors = collect_mul_factors(ctx, arg);
            if factors.len() <= 1 {
                return None;
            }

            let mut sum = make_log_expr(ctx, base, factors[0]);
            for &factor in &factors[1..] {
                let log_factor = make_log_expr(ctx, base, factor);
                sum = ctx.add(Expr::Add(sum, log_factor));
            }

            Some(LogRewrite {
                rewritten: sum,
                desc: "Auto-expand log product",
            })
        }
        Expr::Div(num, den) => {
            let (num, den) = (*num, *den);
            let log_num = make_log_expr(ctx, base, num);
            let log_den = make_log_expr(ctx, base, den);
            let rewritten = ctx.add(Expr::Sub(log_num, log_den));
            Some(LogRewrite {
                rewritten,
                desc: "Auto-expand log quotient",
            })
        }
        Expr::Pow(pow_base, exp) => {
            let (pow_base, exp) = (*pow_base, *exp);
            let log_base = make_log_expr(ctx, base, pow_base);
            let rewritten = smart_mul(ctx, exp, log_base);
            Some(LogRewrite {
                rewritten,
                desc: "Auto-expand log power",
            })
        }
        _ => None,
    }
}

/// Recursively expand logarithms and collect positivity assumptions as raw expressions.
///
/// Expansion rules:
/// - `log(b, x*y) -> log(b, x) + log(b, y)`
/// - `log(b, x/y) -> log(b, x) - log(b, y)`
///
/// Every expanded multiplicative/division factor is returned in `assumed_positive`.
/// The caller can map those expressions to domain-assumption events in its own layer.
pub fn expand_logs_collect_positive_assumptions(
    ctx: &mut Context,
    expr: ExprId,
) -> ExpandedLogAssumptionPlan {
    fn expand(ctx: &mut Context, expr: ExprId) -> (ExprId, Vec<ExprId>) {
        enum LogKind {
            LogFn(usize, Vec<ExprId>),
            Add(ExprId, ExprId),
            Sub(ExprId, ExprId),
            Mul(ExprId, ExprId),
            Div(ExprId, ExprId),
            Neg(ExprId),
            Pow(ExprId, ExprId),
            Other,
        }

        let kind = match ctx.get(expr) {
            Expr::Function(name, args) => LogKind::LogFn(*name, args.clone()),
            Expr::Add(l, r) => LogKind::Add(*l, *r),
            Expr::Sub(l, r) => LogKind::Sub(*l, *r),
            Expr::Mul(l, r) => LogKind::Mul(*l, *r),
            Expr::Div(l, r) => LogKind::Div(*l, *r),
            Expr::Neg(e) => LogKind::Neg(*e),
            Expr::Pow(b, e) => LogKind::Pow(*b, *e),
            _ => LogKind::Other,
        };
        let mut assumed_positive = Vec::new();

        let result = match kind {
            LogKind::LogFn(name, ref args)
                if matches!(
                    ctx.builtin_of(name),
                    Some(BuiltinFn::Ln) | Some(BuiltinFn::Log)
                ) =>
            {
                // Sentinel reserved by engine/parser bridge for base-10 `log(x)`.
                let sentinel_log10 = ExprId::from_raw(u32::MAX - 1);
                let builtin = ctx.builtin_of(name);
                let (base, arg) = if builtin == Some(BuiltinFn::Log) && args.len() == 1 {
                    (sentinel_log10, args[0])
                } else if let Some((base, arg)) = extract_log_base_argument(ctx, expr) {
                    (base, arg)
                } else {
                    let mut new_args = Vec::with_capacity(args.len());
                    for a in args {
                        let (expanded, sub_assumed_positive) = expand(ctx, *a);
                        new_args.push(expanded);
                        assumed_positive.extend(sub_assumed_positive);
                    }
                    return (ctx.add(Expr::Function(name, new_args)), assumed_positive);
                };

                let (expanded_arg, sub_assumed_positive) = expand(ctx, arg);
                assumed_positive.extend(sub_assumed_positive);
                let (lhs, rhs) = if let Expr::Mul(lhs, rhs) = ctx.get(expanded_arg) {
                    (*lhs, *rhs)
                } else if let Expr::Div(num, den) = ctx.get(expanded_arg) {
                    let (num, den) = (*num, *den);
                    assumed_positive.push(num);
                    assumed_positive.push(den);

                    let log_num = make_log_expr(ctx, base, num);
                    let log_den = make_log_expr(ctx, base, den);
                    let diff = ctx.add(Expr::Sub(log_num, log_den));
                    let (final_result, more_assumed_positive) = expand(ctx, diff);
                    assumed_positive.extend(more_assumed_positive);
                    return (final_result, assumed_positive);
                } else {
                    return (make_log_expr(ctx, base, expanded_arg), assumed_positive);
                };

                assumed_positive.push(lhs);
                assumed_positive.push(rhs);

                let log_lhs = make_log_expr(ctx, base, lhs);
                let log_rhs = make_log_expr(ctx, base, rhs);
                let sum = ctx.add(Expr::Add(log_lhs, log_rhs));
                let (final_result, more_assumed_positive) = expand(ctx, sum);
                assumed_positive.extend(more_assumed_positive);
                return (final_result, assumed_positive);
            }
            LogKind::Add(l, r) => {
                let (el, le) = expand(ctx, l);
                let (er, re) = expand(ctx, r);
                assumed_positive.extend(le);
                assumed_positive.extend(re);
                ctx.add(Expr::Add(el, er))
            }
            LogKind::Sub(l, r) => {
                let (el, le) = expand(ctx, l);
                let (er, re) = expand(ctx, r);
                assumed_positive.extend(le);
                assumed_positive.extend(re);
                ctx.add(Expr::Sub(el, er))
            }
            LogKind::Mul(l, r) => {
                let (el, le) = expand(ctx, l);
                let (er, re) = expand(ctx, r);
                assumed_positive.extend(le);
                assumed_positive.extend(re);
                ctx.add(Expr::Mul(el, er))
            }
            LogKind::Div(l, r) => {
                let (el, le) = expand(ctx, l);
                let (er, re) = expand(ctx, r);
                assumed_positive.extend(le);
                assumed_positive.extend(re);
                ctx.add(Expr::Div(el, er))
            }
            LogKind::Pow(b, e) => {
                let (eb, be) = expand(ctx, b);
                let (ee, exp_e) = expand(ctx, e);
                assumed_positive.extend(be);
                assumed_positive.extend(exp_e);
                ctx.add(Expr::Pow(eb, ee))
            }
            LogKind::Neg(e) => {
                let (ee, sub_e) = expand(ctx, e);
                assumed_positive.extend(sub_e);
                ctx.add(Expr::Neg(ee))
            }
            LogKind::LogFn(name, args) => {
                let mut new_args = Vec::with_capacity(args.len());
                for a in args {
                    let (expanded, sub_assumed_positive) = expand(ctx, a);
                    new_args.push(expanded);
                    assumed_positive.extend(sub_assumed_positive);
                }
                ctx.add(Expr::Function(name, new_args))
            }
            LogKind::Other => expr,
        };

        (result, assumed_positive)
    }

    let (rewritten, assumed_positive) = expand(ctx, expr);
    ExpandedLogAssumptionPlan {
        rewritten,
        assumed_positive,
    }
}

