//! Final assembly for session-backed `eval-json` outputs.

use cas_api_models::EvalJsonOutput;

use crate::eval_json_finalize_expr::finalize_expr_like_eval_json_output;
use crate::eval_json_finalize_input::EvalJsonFinalizeInput;
use crate::eval_json_finalize_nonexpr::{finalize_bool_output, finalize_solution_set_output};

pub(crate) fn finalize_eval_json_output(
    input: EvalJsonFinalizeInput<'_>,
) -> Result<EvalJsonOutput, String> {
    let EvalJsonFinalizeInput {
        result,
        ctx,
        max_chars,
        input,
        input_latex,
        steps_mode,
        steps,
        solve_steps,
        warnings,
        required_conditions,
        required_display,
        raw_steps_count,
        raw_solve_steps_count,
        budget_preset,
        strict,
        domain,
        timings_us,
        context_mode,
        branch_mode,
        expand_policy,
        complex_mode,
        const_fold,
        value_domain,
        complex_branch,
        inv_trig,
        assume_scope,
    } = input;

    match result {
        cas_solver::EvalResult::SolutionSet(solution_set) => Ok(finalize_solution_set_output(
            ctx,
            solution_set,
            input,
            input_latex,
            steps_mode,
            steps,
            solve_steps,
            warnings,
            required_conditions,
            required_display,
            raw_steps_count + raw_solve_steps_count,
            budget_preset,
            strict,
            domain,
            timings_us,
            context_mode,
            branch_mode,
            expand_policy,
            complex_mode,
            const_fold,
            value_domain,
            complex_branch,
            inv_trig,
            assume_scope,
        )),
        cas_solver::EvalResult::Bool(b) => Ok(finalize_bool_output(
            *b,
            input,
            input_latex,
            steps_mode,
            steps,
            solve_steps,
            warnings,
            required_conditions,
            required_display,
            raw_steps_count,
            budget_preset,
            strict,
            domain,
            timings_us,
            context_mode,
            branch_mode,
            expand_policy,
            complex_mode,
            const_fold,
            value_domain,
            complex_branch,
            inv_trig,
            assume_scope,
        )),
        cas_solver::EvalResult::Expr(e) => finalize_expr_like_eval_json_output(
            ctx,
            *e,
            max_chars,
            input,
            input_latex,
            steps_mode,
            steps,
            solve_steps,
            warnings,
            required_conditions,
            required_display,
            raw_steps_count,
            budget_preset,
            strict,
            domain,
            timings_us,
            context_mode,
            branch_mode,
            expand_policy,
            complex_mode,
            const_fold,
            value_domain,
            complex_branch,
            inv_trig,
            assume_scope,
        ),
        cas_solver::EvalResult::Set(v) if !v.is_empty() => finalize_expr_like_eval_json_output(
            ctx,
            v[0],
            max_chars,
            input,
            input_latex,
            steps_mode,
            steps,
            solve_steps,
            warnings,
            required_conditions,
            required_display,
            raw_steps_count,
            budget_preset,
            strict,
            domain,
            timings_us,
            context_mode,
            branch_mode,
            expand_policy,
            complex_mode,
            const_fold,
            value_domain,
            complex_branch,
            inv_trig,
            assume_scope,
        ),
        _ => Err("No result expression".to_string()),
    }
}
